
public class Tutorial25 {
	public static void main(String[] args){ // método principal
	    //System.out.println(Math.(abs, ceil, floor, max, min, pow, sqrt all can apply after dot seporator)(1));
		System.out.println(Math.abs(-27.7)); // 27.7 (valor absoluto, número sin signo)
		System.out.println(Math.ceil(7.4)); // 8.0 (valor por arriba)
		System.out.println(Math.floor(7.4)); // 7.0 (valor por abajo)
		System.out.println(Math.max(8.6, 5.2)); // 8.6 (valor máximo entre 2 números)
		System.out.println(Math.min(8.6, 5.2)); // 5.2 (valor mínimo entre 2 números)
		System.out.println(Math.pow(5, 3)); // 125.0 (potencia)
		System.out.println(Math.sqrt(9)); // 3.0 (raíz cuadrada)
	}
}
